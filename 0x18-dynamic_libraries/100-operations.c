int add(int i, int k);
int sub(int i, int k);
int mul(int i, int k);
int div(int i, int k);
int mod(int i, int k);

/**
 * add - adds two numbers
 * @i: num1
 * @k: num2
 * Return: result
 */
int add(int i, int k)
{
	return (i + k);
}

/**
 * sub - subs i from k
 * @i: ================
 * @k: =================
 * Return: ===============
 */
int sub(int i, int k)
{
	return (i - k);
}

/**
 * mul - multiplies two nums
 * @i: ================
 * @k: ===============
 * Return: ============
 */
int mul(int i, int k)
{
	return (i * k);
}

/**
 * div - divides i by k
 * @i: ==================
 * @k: ================
 * Return: ==============
 */
int div(int i, int k)
{
	return (i / k);
}

/**
 * mod - modulo of i by k
 * @i: ===============
 * @k: ================
 * Return: ==========
 */
int mod(int i, int k)
{
	return (i % k);
}
